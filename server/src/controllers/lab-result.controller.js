import { asyncHandler } from "../utils/asyncHandler.js";
import { ApiError } from "../utils/ApiError.js";
import { ApiResponse } from "../utils/apiResponse.js";
import { LabResult } from "../models/LabResult.js";

// @route POST /api/v1/lab-results/upload
// @desc Upload a lab document. Expected strictly from DOCTOR portal
export const uploadLabResult = asyncHandler(async (req, res) => {
  const { patientId, reportTitle, notes } = req.body;
  if (!patientId || !reportTitle) throw new ApiError(400, "Patient ID and Report Title are mandatory");
  if (!req.file) throw new ApiError(400, "Document file payload is missing");

  // Since it requires Doctor ONLY middleware, req.user._id is explicitly the physician executing the upload.
  const labResult = await LabResult.create({
    patientId,
    doctorId: req.user._id,
    reportTitle,
    reportFile: `/uploads/${req.file.filename}`, // Using active Multer public static directory architecture
    notes
  });

  return res.status(201).json(new ApiResponse(201, labResult, "Lab Result successfully provisioned to patient."));
});

// @route GET /api/v1/lab-results/patient/:patientId
// @desc Get lab results specifically for the patient. (Validated to ensure patient ONLY queries their own)
export const getPatientLabResults = asyncHandler(async (req, res) => {
  const { patientId } = req.params;

  // Security Verification: Logged-in Patient must strictly match the parameter
  if (patientId !== req.user._id.toString()) {
     throw new ApiError(403, "Forbidden. Can only fetch personal health reports.");
  }

  const reports = await LabResult.find({ patientId }).populate("doctorId", "name email").sort("-createdAt");
  return res.status(200).json(new ApiResponse(200, reports, "Health Reports Array loaded safely."));
});

// @route GET /api/v1/lab-results/doctor
// @desc Get all lab results generated by physical doctor
export const getDoctorLabResults = asyncHandler(async (req, res) => {
  // Returns exclusively physical documents this physician uploaded tracking patients natively
  const reports = await LabResult.find({ doctorId: req.user._id }).populate("patientId", "name email").sort("-createdAt");
  return res.status(200).json(new ApiResponse(200, reports, "Physician Lab Uploads generated."));
});
